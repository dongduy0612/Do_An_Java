    private void txtMaKMKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMaKMKeyReleased
        // TODO add your handling code here:
        String sql = "select *from khachHang,loaiKh where khachhang.maloaikh=loaikh.maloaikh and makh=?";
        try {
            dataBase.getConnection();
            PreparedStatement pre = dataBase.getCon().prepareStatement(sql);
            pre.setString(1, txtMaKM.getText());
            ResultSet rs = pre.executeQuery();
            if (rs.next()) {
                lbTenKH.setText(rs.getString("tenkh"));
                lbLoaiKH.setText(rs.getString("tenloaikh"));
                lbGiamGia.setText("" + rs.getFloat("giamgia"));
                txtGiamGia.setText(lbGiamGia.getText());
                txtTongCong1.setText(txtTongCong.getText());
                float tienGiam = 0;
                float tongCong = (Float.parseFloat(txtTongCong.getText()));
                float giamGia = (Float.parseFloat(txtGiamGia.getText()));
                tienGiam = tongCong * giamGia / (float) 100;
                txtThanhTien.setText("" + (tongCong - tienGiam));
            } else {
                txtGiamGia.setText("");
                txtTongCong1.setText("");
                lbTenKH.setText("...");
                lbLoaiKH.setText("...");
                lbGiamGia.setText("...");
                txtThanhTien.setText(txtTongCong.getText());
            }
        } catch (Exception e) {
        }
        dataBase.close();
    }//GEN-LAST:event_txtMaKMKeyReleased

