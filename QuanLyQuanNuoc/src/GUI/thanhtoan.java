    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (hoTro.ktraRong(txtTienKhachDua) || hoTro.ktraRong(txtThanhTien)) {
            return;
        }
        String ktr = "HD\\d{2}";
        System.out.println(txtMaHD.getText());
        if (!txtMaHD.getText().toUpperCase().matches(ktr)) {
            JOptionPane.showMessageDialog(null, "Hoá đơn có dạng hdxx", "Thông báo", 1);
            txtMaHD.requestFocus();
            return;
        }

        float tienDua = Float.parseFloat(txtTienKhachDua.getText());

        float tienTraLai = tienDua - Float.parseFloat(txtThanhTien.getText());
        if (tienTraLai < 0) {
            JOptionPane.showMessageDialog(null, "Khách đưa không đủ tiền", "Thông báo", 1);
            txtTienKhachDua.requestFocus();
            return;
        }
        txtTienThua.setText("" + tienTraLai);
        String mahd = txtMaHD.getText();
        float thanhToan = Float.parseFloat(txtThanhTien.getText());
        int j = hoaDonDAO.insert(mahd, thanhToan, maNV);
        if (j != -1) {
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                chiTietHDDAO.insert(mahd, modelTb.getValueAt(i, 0).toString(), Integer.parseInt(modelTb.getValueAt(i, 3).toString()), Float.parseFloat(modelTb.getValueAt(i, 4).toString()));
            }
            createNew();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

